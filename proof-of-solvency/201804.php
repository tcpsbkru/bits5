                <h2>How to Read the Proof of Solvency Results</h2>
                <p>The first two tables below display our liabilities: the total amount of each asset held by users on the ICONOMI platform and the total amount of each asset held by DAAs on the date of the proof of solvency.</p>
                <p>The summary table compares our liabilities to our reserves and was verified by a Big Four professional services firm.</p>
                <p>Exchange rates to USD were calculated on the date of the audit at 7AM UTC.</p>
