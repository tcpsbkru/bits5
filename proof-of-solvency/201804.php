            <div class="intro__about-process">
                <h2>About the Proof of Solvency Process</h2>
                <p>The proof of solvency combined a proof of liabilities and a proof of reserves to prove that ICONOMI's reserves are greater than its liabilities. Our proof of liabilities used Merkle trees, which enable a transparent way for each user to confirm his or her balance without revealing any personal information to the public.</p>
                <p>Read more about the process in our <a href="https://medium.com/iconominet/proof-of-solvency-technical-overview-d1d0e8a8a0b8" target="_blank">blog post</a>.</p>
            </div>

